package JiHe;

import java.util.TreeSet;

/*
TreeSet	如果元素具备自然元素的特性，那么会按照元素自然顺序的特性进行排序存储
	


*/
public class TreeSetLearn {

	public static void main(String[] args) {
		TreeSet tree = new TreeSet();
		tree.add(1);
		tree.add(10);
		tree.add(7);
		tree.add(19);
		tree.add(9);
		System.out.println(tree);
	}

}
